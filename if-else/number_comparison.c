//Given two numbers, compare them for greater, smaller and equal to
int main(){
	int x=90;
	int y=90;
	
	
	if(x<y){
	   printf("%d is less than %d",x,y);
	} 
	else if(x>y){
		printf("%d is greater than %d",x,y);
	}
	else {
		printf("%d is equal to %d",x,y);
    }
	
    printf("\nEND OF PROGRAM");
}
  
  